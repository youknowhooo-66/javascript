let notaI, notaII, notaIII, promedio, x = 3.0, valorfinal

notaI = Number(prompt('Digite o valor da primeira nota: '))
notaII = Number(prompt('Digite o valor da segunda nota: '))
notaIII = Number(prompt('Digite o valor da terceira nota: '))

promedio = notaI + notaII + notaIII
valorfinal = promedio / x

alert('O promédio das notas é ' + valorfinal + '.')